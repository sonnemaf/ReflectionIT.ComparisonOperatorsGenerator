namespace ReflectionIT.ComparisonOperatorsGenerator;

public interface ICsFileBuilder : IDisposable
{
    ICsFileBuilder AddPreprocessorDirectives();
    ICsFileBuilder AddAutoGeneratedHeader(string by);
    ICsFileBuilder AddStaticUsing(string usingStatement);
    ICsFileBuilder AddUsing(string usingStatement);
    ICsFileBuilder AddFileScopedNamespace(string namespaceName, bool condition);
    ICsFileBuilder AddNamespace(string namespaceName, bool condition);
    ICsFileBuilder AddCommentLine(string line);
    ICsFileBuilder AddXmlCommentLines(params ReadOnlySpan<string> lines);
    ICsFileBuilder AddEmptyLine(bool condition = true);
    ICsFileBuilder AddStatements(params ReadOnlySpan<string> lines);
    ICsFileBuilder AddStatementsIf(bool condition, params ReadOnlySpan<string> lines);
    ICsFileBuilder AddStatementAndStartBlock(string line);
    ICsFileBuilder AddIfBlock(string condition, params ReadOnlySpan<string> lines);
    ICsFileBuilder EndBlock();
    ICsFileBuilder EndNamespace();

    ICsFileBuilder AddGeneratedAttributes(
       string generatorName,
       bool useFullyQualifiedTypeNames = true,
       bool includeNonUserCodeAttributes = true);
    string Build();
}

    public const string Attribute = Header + """

        #if COMPARISON_OPERATORS_GENERATOR_EMBED_ATTRIBUTES
        
        /// <summary>
        /// An attribute to indicate that comparison operators should be generated for the target class, struct or record
        /// <para>
